    public static String encodeHexStr_OneByte(char data) {
        return new String(encodeHex_OneByte(data));
    }

    public static char[] encodeHex_OneByte(char data) {
        char[] out = new char[2];
        out[0] = DIGITS_LOWER[(0xF0 & data) >>> 4]; //大寫
        out[1] = DIGITS_LOWER[0x0F & data];   //大寫
        return out;
    }
